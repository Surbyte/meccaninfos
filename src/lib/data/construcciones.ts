export function getConstruccionesByCategoria(): Record<string, Construccion[]> {
  const categorias: Record<string, Construccion[]> = {}
  for (const c of construcciones) {
    if (!categorias[c.categoria]) categorias[c.categoria] = []
    categorias[c.categoria].push(c)
  }
  return categorias
}

export function getConstruccionBySlug(slug: string): Construccion | undefined {
  return construcciones.find((c) => c.slug === slug)
}

export function getCategoriasConstrucciones(): string[] {
  return [...new Set(construcciones.map((c) => c.categoria))]
}
